package com.info.Helpers;


public class RankingResults {

    private String name = "";
    private String score = "";
    private String diveNumber = "";

    public String getName() {
        return name;
    }

    public String getDiveNumber() { return diveNumber; }

    public void setDiveNumber(String diveNumber) { this.diveNumber = diveNumber; }

    public void setName(String name) {
        this.name = name;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }



}
